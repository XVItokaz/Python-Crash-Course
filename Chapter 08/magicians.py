# List of magician's names
magicians = ["Harry Houdini", "David Copperfield", "Penn Jillette", "Teller", "Dynamo", "Criss Angel"]

# Function to print the name of each magician in the list
def show_magicians(magician_list):
    for magician in magician_list:
        print(magician)

# Call the function with the list of magicians
show_magicians(magicians)
